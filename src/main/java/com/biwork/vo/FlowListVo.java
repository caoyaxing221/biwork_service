package com.biwork.vo;

public class FlowListVo {
	String id;
	String name;
	String isBatch;
	String visibleAll;
	String templateNo;
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the isBatch
	 */
	public String getIsBatch() {
		return isBatch;
	}
	/**
	 * @param isBatch the isBatch to set
	 */
	public void setIsBatch(String isBatch) {
		this.isBatch = isBatch;
	}
	/**
	 * @return the visibleAll
	 */
	public String getVisibleAll() {
		return visibleAll;
	}
	/**
	 * @param visibleAll the visibleAll to set
	 */
	public void setVisibleAll(String visibleAll) {
		this.visibleAll = visibleAll;
	}
	/**
	 * @return the templateNo
	 */
	public String getTemplateNo() {
		return templateNo;
	}
	/**
	 * @param templateNo the templateNo to set
	 */
	public void setTemplateNo(String templateNo) {
		this.templateNo = templateNo;
	}
}
