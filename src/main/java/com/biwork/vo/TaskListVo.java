package com.biwork.vo;

public class TaskListVo {
	String id;
	String name;
	String addressCount;
	String endTime;
	String leftSeconds;
	String state;
	String type;
	String count;
	String token;
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the addressCount
	 */
	public String getAddressCount() {
		return addressCount;
	}
	/**
	 * @param addressCount the addressCount to set
	 */
	public void setAddressCount(String addressCount) {
		this.addressCount = addressCount;
	}
	/**
	 * @return the endTime
	 */
	public String getEndTime() {
		return endTime;
	}
	/**
	 * @param endTime the endTime to set
	 */
	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}
	/**
	 * @return the leftSeconds
	 */
	public String getLeftSeconds() {
		return leftSeconds;
	}
	/**
	 * @param leftSeconds the leftSeconds to set
	 */
	public void setLeftSeconds(String leftSeconds) {
		this.leftSeconds = leftSeconds;
	}
	/**
	 * @return the state
	 */
	public String getState() {
		return state;
	}
	/**
	 * @param state the state to set
	 */
	public void setState(String state) {
		this.state = state;
	}
	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
	/**
	 * @return the count
	 */
	public String getCount() {
		return count;
	}
	/**
	 * @param count the count to set
	 */
	public void setCount(String count) {
		this.count = count;
	}
	/**
	 * @return the token
	 */
	public String getToken() {
		return token;
	}
	/**
	 * @param token the token to set
	 */
	public void setToken(String token) {
		this.token = token;
	}
}
